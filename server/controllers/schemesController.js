const CATEGORIES = [...new Set(SCHEMES.map(s => s.category))];

const getSchemes = asyncHandler(async (req, res) => {
    const { category = '', state = '', search = '', featured = '' } = req.query;
    let filtered = [...SCHEMES];

    if (category) filtered = filtered.filter(s => s.category.toLowerCase() === category.toLowerCase());
    if (state && state !== 'all') filtered = filtered.filter(s => s.states === 'all' || s.states.toLowerCase().includes(state.toLowerCase()));
    if (search) {
        const q = search.toLowerCase();
        filtered = filtered.filter(s =>
            s.name.toLowerCase().includes(q) ||
            s.fullName.toLowerCase().includes(q) ||
            s.benefit.toLowerCase().includes(q) ||
            s.category.toLowerCase().includes(q) ||
            s.nameHindi?.includes(q) ||
            s.benefitHindi?.includes(q)
        );
    }
    if (featured === 'true') filtered = filtered.filter(s => s.featured);

    res.status(200).json({ success: true, count: filtered.length, categories: CATEGORIES, data: filtered });
});

module.exports = { getSchemes };
